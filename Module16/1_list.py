# Задача 3. Контроль
# В любой компании есть список сотрудников. Руководство одной компании хочет знать, на рабочем месте ли сейчас
# сотрудник. У каждого сотрудника есть пропуск со своим ID-номером (это положительное число), список активных
# пропусков сотрудников известен заранее.
#
# Напишите программу, которая сначала запрашивает у пользователя количество сотрудников в офисе, ID их
# пропусков, а затем запрашивает ID пропуска, который нужно найти в этом списке. Если такой есть, то
# вывести «Сотрудник на месте», а иначе «Сотрудник не работает!».

count_workers = int(input('Количество сотрудников в офисе? '))
ID_workers = []

for _ in range(count_workers):
    id_worker = int(input('ID сотрудника: '))
    ID_workers.append(id_worker)

found_id = int(input('Какой ID ищем? '))
is_found = False

for id_worker in ID_workers:
    if id_worker == found_id:
        is_found = True
        break

if is_found:
    print('Сотрудник на месте')
else:
    print('Сотрудник не работает!')