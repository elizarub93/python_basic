# условные операторы. Можно использовать цикл while, но постарайтесь всё-таки решить с помощью конструкции for in range.
# Не нужно искать решение в интернете, попробуйте подумать сами, в следующем уроке мы обязательно разберём эту задачу.

number = int(input('Введите число: '))

for num in range(number // 2):
    num = 2 * num + 1
    print(num, ' ** 2 =', num ** 2)
