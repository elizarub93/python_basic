# Задача 4. Калькулятор скидки
# Васин друг переехал в новую квартиру, и ему нужно купить три стула в разные комнаты. Цены на стулья разные,
# а в некоторых магазинах есть скидки. Друг хочет заказать у Васи калькулятор скидки, чтобы проще ориентироваться в ценах.
#
# Напишите программу, которая запрашивает три стоимости товара и вычисляет сумму чека. Если сумма чека превышает 10 000 руб.,
# нужно вычесть из этой суммы скидку 10% (умножить на 10, разделить на 100). Итоговая сумма должна выводиться на экран.
sum1 = int(input('Введите стоимость первого товара: '))
sum2 = int(input('Введите стоимость второго товара: '))
sum3 = int(input('Введите стоимость третьего товара: '))
sum = sum1 + sum2 + sum3

if sum > 10000:
    sum -= sum * 10 / 100
print('Итоговая сумма: ', sum)

