# Задача 6. Игра в кубики
# Вася понимает, как важен отдых после тяжёлого рабочего дня, поэтому часто ходит в местный бар с друзьями.
# Владелец бара любит азартные игры и иногда предлагает посетителям с ним сыграть.
# Вася избегает азартные игры, но предложил владельцу купить у него программу для расчёта результатов игры,
# которую можно выводить на экраны бара.
#
# Напишите программу: на вход в неё подаётся два числа. Если первое число больше или равно второму, то нужно
# вывести на экран их разность и отдельной строкой: «Игрок платит». В противном случае, вывести их сумму и
# отдельной строкой: «Владелец платит». Последней строкой на экран должна быть выведена фраза: «Игра окончена».
#
# Пример:
# Кубик Кости: 3
# Кубик владельца: 4
# Сумма: 7
# Владелец платит
# Игра окончена
num1 = int(input('Введите первое число: '))
num2 = int(input('Введите второе число: '))
if num1 >= num2:
    print(num1 - num2)
    print('Игрок платит!')
else:
    print(num1 + num2)
    print('Владелец платит')
print('Игра окончена')

# Задача 7. Хватит ли зарплаты
# Вася набрался опыта и решил поискать вакансию с большей зарплатой. Его привлекла вакансия со странной формулой для
# расчёта заработной платы:
#
# Он хочет понять, сколько часов нужно отработать, чтобы хватило на погашение кредита и еду.
# Напишите программу, которая запрашивает у пользователя три числа: количество отработанных часов, остаток по кредиту
# и количество денег на еду. После этого рассчитывается зарплата по формуле. Если зарплата больше или равна сумме,
# которая требуется на кредит и еду, то выводится сообщение: «Часов хватает. Можно отдохнуть». В противном случае:
# «Часов не хватает. Придётся работать больше!»
#
# Пример:
# Введите отработанные часы: 80
# Введите остаток по кредиту: 1000
# Введите траты на еду: 5000
# Часов не хватает. Придётся работать больше!

hours = int(input('Введите отработанные часы: '))
credit = int(input('Введите остаток по кредиту: '))
eat = int(input('Введите траты на еду: '))
income = (200 * hours / 2 ** 3) + hours
if income >= credit + eat:
    print('Часов хватает. Можно отдохнуть')
else:
    print('Часов не хватает. Придётся работать больше!')

